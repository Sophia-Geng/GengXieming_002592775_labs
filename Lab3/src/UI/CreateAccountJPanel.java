    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        int balance;
        String routingNumber=fieldRoutingNum.getText();
        String accountNumber=fieldAccountNum.getText();
        String bankName=fieldBankName.getText();
        
        if(routingNumber.isBlank()||bankName.isBlank()||accountNumber.isBlank()){
        JOptionPane.showMessageDialog(this, "All fields are mandatory","Error",JOptionPane.ERROR_MESSAGE);
         return;
    }
        else{
             JOptionPane.showMessageDialog(this, "Successfully createdÔºÅ","Create",JOptionPane.INFORMATION_MESSAGE);
        }
        
        try{
            balance=Integer.parseInt(fieldBalance.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please check the balance input", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Account a=accountdirectory.addAccount();
        a.setRoutingNumber(routingNumber);
        a.setBalance(balance);
        a.setBankName(bankName);
        a.setAccountNumber(accountNumber);


        fieldRoutingNum.setText("");
        fieldAccountNum.setText("");
        fieldBankName.setText("");
        fieldBalance.setText("");


    }//GEN-LAST:event_btnCreateActionPerformed


